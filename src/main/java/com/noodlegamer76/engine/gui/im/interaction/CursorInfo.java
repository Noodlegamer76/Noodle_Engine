package com.noodlegamer76.engine.gui.im.interaction;

public class CursorInfo {
    private int x;
    private int y;

    public CursorInfo(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
